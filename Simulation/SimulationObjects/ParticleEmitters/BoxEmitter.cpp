#include <Noodle/Core/Particle/ParticleEmitters/BoxEmitter.h>
#include <Noodle/Core/Particle/ObjectBuilders/BoxBuilder.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BoxEmitter::set_box_params(const Vec3& box_min_, const Vec3& box_max_)
{
    box_min = box_min_;
    box_max = box_max_;

    box_initialized = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BoxEmitter::build_emitter()
{
    __NOODLE_ASSERT(check_all_params());

    monitor.printLog("Building emitter of shape Box:");

    particle_generator = new BoxBuilder(domainParams, samplingParams, sdf_boundary);

    BoxBuilder* generator = (BoxBuilder*)particle_generator;
    generator->set_box_params(box_min, box_max);
    generator->generate_particle(emitter_particles, particle_radius);
}


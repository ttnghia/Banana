//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereBoundary3D::setParameters(const nlohmann::json& jParams)
{
    __BNN_ASSERT(jParams.find("Center") != jParams.end());
    __BNN_ASSERT(jParams.find("Radius") != jParams.end());

    Vec3r center;
    Real  radius;
    JSONHelpers::readVector(jParams, center, "Center");
    JSONHelpers::readValue(jParams, radius, "Radius");

    setSphere(center, radius);
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereBoundary3D::generateBoundaryParticles(Real spacing, Int numBDLayers, bool saveCache)
{}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
bool SphereBoundary3D::constrainToBoundary(Vec3r& ppos, Vec3r& pvel)
{
    return false;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereBoundary3D::setSphere(const Vec3r& center, Real radius)
{
    SharedPtr<GeometryObject3D::SphereObject> sphere = std::static_pointer_cast<GeometryObject3D::SphereObject>(m_GeometryObj);
    __BNN_ASSERT(sphere != nullptr);
    sphere->setSphere(center, radius);
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
const Vec3r& SphereBoundary3D::center() const
{
    SharedPtr<GeometryObject3D::SphereObject> sphere = std::static_pointer_cast<GeometryObject3D::SphereObject>(m_GeometryObj);
    __BNN_ASSERT(sphere != nullptr);
    return sphere->center();
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
Real SphereBoundary3D::radius() const
{
    SharedPtr<GeometryObject3D::SphereObject> sphere = std::static_pointer_cast<GeometryObject3D::SphereObject>(m_GeometryObj);
    __BNN_ASSERT(sphere != nullptr);
    return sphere->radius();
}


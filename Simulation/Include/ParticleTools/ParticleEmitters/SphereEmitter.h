#ifndef SPHERE_EMITTER_H
#define SPHERE_EMITTER_H

#include <Noodle/Core/Particle/ParticleEmitters/ParticleEmitter.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class SphereEmitter : public ParticleEmitter
{
public:
    SphereEmitter(DomainParameters* domainParams_,
                  ParticleSamplingParameters* samplingParams_,
                  const Array3_Real& sdf_boundary_,
                  Real particle_radius_) :
        ParticleEmitter(domainParams_, samplingParams_, sdf_boundary_, particle_radius_),
        sphere_initialized(false)
    {}

    void set_hollow_sphere_params(const Vec3& center_, Real inner_radius_,
                                  Real outer_radius_);

protected:
    virtual bool check_all_params() override
    {
        return (params_initialized && sphere_initialized);
    }

    virtual void build_emitter() override;
    virtual void add_velocity(const Vec_Vec3& new_particles,
                              Vec_Vec3& velocity) override;

    Vec3 sphere_center;
    Real outer_radius;
    Real inner_radius;

    bool sphere_initialized;
};

#endif // SPHERE_EMITTER_H

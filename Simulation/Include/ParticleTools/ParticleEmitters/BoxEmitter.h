#ifndef BOX_EMITTER_H
#define BOX_EMITTER_H

#include <Noodle/Core/Particle/ParticleEmitters/ParticleEmitter.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class BoxEmitter : public ParticleEmitter
{
public:
    BoxEmitter(DomainParameters* domainParams_,
               ParticleSamplingParameters* samplingParams_,
               const Array3_Real& sdf_boundary_,
               Real particle_radius_) :
        ParticleEmitter(domainParams_, samplingParams_, sdf_boundary_, particle_radius_),
        box_initialized(false)
    {}

    void set_box_params(const Vec3& box_min_, const Vec3& box_max_);

protected:
    virtual bool check_all_params() override
    {
        return (params_initialized && box_initialized);
    }

    virtual void build_emitter() override;

    ////////////////////////////////////////////////////////////////////////////////
    Vec3 box_min;
    Vec3 box_max;
    bool box_initialized;
};

#endif // BOX_EMITTER_H
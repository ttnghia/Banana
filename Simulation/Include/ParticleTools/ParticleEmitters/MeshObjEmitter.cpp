#include <Noodle/Core/Particle/ObjectBuilders/TriMeshBuilder.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void TriMeshEmitter::set_mesh_params(const std::string& mesh_file_,
                                     const Vec3& mesh_center_, Real mesh_scale_)
{
    __NOODLE_ASSERT(!mesh_initialized);

    mesh_file = mesh_file_;
    mesh_center = mesh_center_;
    mesh_scale = mesh_scale_;

    mesh_initialized = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void TriMeshEmitter::build_emitter()
{
    __NOODLE_ASSERT(check_all_params());

    monitor.printLog("Building emitter of shape from mesh file:");

    particle_generator = new TriMeshBuilder(domainParams, samplingParams, sdf_boundary);

    TriMeshBuilder* generator = (TriMeshBuilder*)particle_generator;
    generator->set_mesh_params(mesh_file, mesh_center, mesh_scale);
    generator->generate_particle(emitter_particles, particle_radius);
}


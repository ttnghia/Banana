//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereEmitter::set_hollow_sphere_params(const Vec3& center_, Real inner_radius_,
                                             Real outer_radius_)
{
    sphere_center = center_;
    inner_radius = inner_radius_;
    outer_radius = outer_radius_;

    sphere_initialized = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereEmitter::build_emitter()
{
    __NOODLE_ASSERT(check_all_params());

    monitor.printLog("Building emitter of shape HollowSphere:");

    particle_generator = new HollowSphereBuilder(domainParams, samplingParams, sdf_boundary);

    HollowSphereBuilder* generator = (HollowSphereBuilder*)particle_generator;
    generator->set_hollow_sphere_params(sphere_center, inner_radius, outer_radius);
    generator->generate_particle(emitter_particles, particle_radius);
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void SphereEmitter::add_velocity(const Vec_Vec3& new_particles, Vec_Vec3& velocity)
{
    velocity.reserve(velocity.size() + new_particles.size());

#ifdef __Using_Eigen_Lib__
    Real mag_vel = initial_velocity.norm();
#else
    Real mag_vel = glm::length(initial_velocity);
#endif

    for(size_t i = 0; i < new_particles.size(); ++i)
    {
#ifdef __Using_Eigen_Lib__
        velocity.push_back(mag_vel * (new_particles[i] - sphere_center).normalized());
#else
        velocity.push_back(mag_vel * glm::normalize(new_particles[i] - sphere_center));
#endif
    }
}

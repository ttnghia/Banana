#ifndef TRIMESH_BUILDER_H
#define TRIMESH_BUILDER_H

#include <Noodle/Core/Particle/ObjectBuilders/ObjectBuilder.h>
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class TriMeshBuilder : public ObjectBuilder
{
public:
    TriMeshBuilder(DomainParameters* domainParams_,
                   ParticleSamplingParameters* samplingParams_,
                   const Array3_Real& sdf_boundary_) :
        ObjectBuilder(domainParams_, samplingParams_, sdf_boundary_),
        mesh_initialized(false),
        mesh_loaded(false)
    {
        set_obj_name("mesh");
    }

    void set_mesh_params(const std::string& mesh_file_, const Vec3& mesh_center_,
                         Real mesh_scale_);

protected:
    virtual std::string get_generator_name() override
    {
        return std::string("TriMeshBuilder");
    }

    virtual bool check_all_params() override
    {
        return mesh_initialized;
    }

    virtual void compute_aabb() override;
    virtual void compute_sdf(bool negative_inside = true) override;

    void load_mesh();

    ////////////////////////////////////////////////////////////////////////////////
    bool mesh_loaded;
    bool mesh_initialized;
    Vec3 mesh_center;
    Real mesh_scale;
    std::string mesh_file;
    std::vector<Vec3> vertex_list;
    std::vector<Vec3ui> face_list;
};

#endif // TRIMESH_BUILDER_H
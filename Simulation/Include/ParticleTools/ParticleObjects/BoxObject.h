//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
template<class RealType>
class BoxObject : public ParticleObject<RealType>
{
public:
    BoxObject()  = default;
    ~BoxObject() = default;

    void setBox(const Vec3RealType& boxMin, const Vec3RealType& boxMax) { m_BoxMin = boxMin; m_BoxMax = boxMax; }

protected:
    virtual void makeObj(RealType particleRadius);

    Vec3<RealType> m_BoxMin = Vec3<RealType>(0);
    Vec3<RealType> m_BoxMax = Vec3<RealType>(0);
};


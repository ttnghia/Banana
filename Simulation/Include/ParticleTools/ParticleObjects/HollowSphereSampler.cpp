//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void HollowSphereBuilder::set_hollow_sphere_params(const Vec3& center_,
                                                   Real inner_radius_, Real outer_radius_)
{
    sphere_center = center_;
    inner_radius = inner_radius_;
    outer_radius = outer_radius_;

    ////////////////////////////////////////////////////////////////////////////////
    monitor.printLog("Set sphere center: " + NumberHelpers::toString(sphere_center) +
                     ", inner radius: " + NumberHelpers::formatWithCommas(inner_radius, 5) +
                     ", outer radius: " + NumberHelpers::formatWithCommas(outer_radius, 5));

    sphere_initialized = true;
    invalidate_data();
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
Real HollowSphereBuilder::object_sdf_value(const Vec3& pos)
{
    return SignDistanceField::sign_distance_hollow_sphere(pos, sphere_center, inner_radius,
                                                          outer_radius);
}





#ifndef TRIMESH_EMITTER_H
#define TRIMESH_EMITTER_H

#include <Noodle/Core/Particle/ParticleEmitters/ParticleEmitter.h>
#include <Noodle/Core/Global/Macros.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class TriMeshEmitter : public ParticleEmitter
{
public:
    TriMeshEmitter(DomainParameters* domainParams_,
                   ParticleSamplingParameters* samplingParams_,
                   const Array3_Real& sdf_boundary_,
                   Real particle_radius_) :
        ParticleEmitter(domainParams_, samplingParams_, sdf_boundary_, particle_radius_) {}

    void set_mesh_params(const std::string& mesh_file_, const Vec3& mesh_center_,
                         Real mesh_scale_);
protected:
    virtual bool check_all_params() override
    {
        return (params_initialized && mesh_initialized);
    }

    virtual void build_emitter() override;

private:
    bool mesh_initialized;
    Vec3 mesh_center;
    Real mesh_scale;
    std::string mesh_file;
};

#endif // TRIMESH_EMITTER_H

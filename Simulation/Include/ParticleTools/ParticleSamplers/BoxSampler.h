#ifndef BOX_BUILDER_H
#define BOX_BUILDER_H

#include <Noodle/Core/Particle/ObjectBuilders/ObjectBuilder.h>
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class BoxBuilder : public ObjectBuilder
{
public:
    BoxBuilder(DomainParameters* domainParams_,
               ParticleSamplingParameters* samplingParams_,
               const Array3_Real& sdf_boundary_) :
        ObjectBuilder(domainParams_, samplingParams_, sdf_boundary_),
        box_initialized(false)
    {
        set_obj_name("box");
    }

    void set_box_params(const Vec3& box_min_, const Vec3& box_max_);

protected:
    virtual std::string get_generator_name() override
    {
        return std::string("BoxBuilder");
    }

    virtual bool check_all_params() override
    {
        return box_initialized;
    }

    virtual void pos_process(Vec_Vec3& particles, Real particle_radius,
                             Real threshold) override;
    virtual void compute_aabb() override;
    virtual Real object_sdf_value(const Vec3& pos) override;

    Vec3 box_min;
    Vec3 box_max;

    bool box_initialized;
};

#endif // BOX_BUILDER_H
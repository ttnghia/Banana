#ifndef BOWL_BUILDER_H
#define BOWL_BUILDER_H

#include <Noodle/Core/Particle/ObjectBuilders/ObjectBuilder.h>
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class BowlBuilder : public ObjectBuilder
{
public:
    BowlBuilder(DomainParameters* domainParams_,
                ParticleSamplingParameters* samplingParams_,
                const Array3_Real& sdf_boundary_) :
        ObjectBuilder(domainParams_, samplingParams_, sdf_boundary_),
        bowl_initialized(false)
    {
        set_obj_name("bowl");
    }

    void set_bowl_params(const Vec3& center_, Real max_y_, Real outer_radius_,
                         Real thickness_);

protected:
    virtual std::string get_generator_name() override
    {
        return std::string("BowlBuilder");
    }

    virtual bool check_all_params() override
    {
        return (bowl_initialized);
    }

    virtual void compute_aabb() override;
    virtual Real object_sdf_value(const Vec3& pos) override;

    Vec3 bowl_center;
    Real max_y;
    Real outer_radius;
    Real inner_radius;
    Real thickness;
    int particle_layers;

    bool bowl_initialized;
};

#endif // BOWL_BUILDER_H
#ifndef HOLLOW_SPHERE_BUILDER_H
#define HOLLOW_SPHERE_BUILDER_H

#include <Noodle/Core/Particle/ObjectBuilders/SphereBuilder.h>
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class HollowSphereBuilder : public SphereBuilder
{
public:
    HollowSphereBuilder(DomainParameters* domainParams_,
                        ParticleSamplingParameters* samplingParams_,
                        const Array3_Real& sdf_boundary_) :
        SphereBuilder(domainParams_, samplingParams_, sdf_boundary_)
    {
        set_obj_name("hollow_sphere");
    }

    void set_hollow_sphere_params(const Vec3& center_, Real inner_radius_,
                                  Real outer_radius_);

protected:
    virtual std::string get_generator_name() override
    {
        return std::string("HollowSphereBuilder");
    }

    virtual Real object_sdf_value(const Vec3& pos) override;

    Real inner_radius;
    Real outer_radius;
};

#endif // HOLLOW_SPHERE_BUILDER_H
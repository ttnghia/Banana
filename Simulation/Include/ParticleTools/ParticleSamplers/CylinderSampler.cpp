//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void CylinderBuilder::set_cylinder_params(const Vec3& base_center,
                                          const Vec3& direction,
                                          Real radius, Real length)
{
    cylinder_base = base_center;
    cylinder_direction = direction;
    cylinder_radius = radius;
    cylinder_length = length;

    ////////////////////////////////////////////////////////////////////////////////
    monitor.printLog("Set cylinder base at: " + NumberHelpers::toString(cylinder_base) +
                     ", direction = " + NumberHelpers::toString(direction));
    monitor.printLog("Cylinder radius = " + NumberHelpers::formatWithCommas(cylinder_radius, 5) +
                     ", length = " + NumberHelpers::formatWithCommas(cylinder_length, 5));

    cylinder_initialized = true;
    invalidate_data();
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void CylinderBuilder::compute_aabb()
{
    Vec3 bbmin = cylinder_base;
#ifdef __Using_Eigen_Lib__
    Vec3 bbmax = cylinder_base + cylinder_length * cylinder_direction.normalized();
#else
    Vec3 bbmax = cylinder_base + cylinder_length * glm::normalize(cylinder_direction);
#endif
    Vec3 tmpBMin, tmpBMax;

    for(int i = 0; i < 3; ++i)
    {
        tmpBMin[i] = fmin(bbmin[i], bbmax[i]);
        tmpBMax[i] = fmax(bbmin[i], bbmax[i]);
    }

    obj_bbmin = tmpBMin - cylinder_radius * Vec3(1.0, 1.0, 1.0);
    obj_bbmax = tmpBMax + cylinder_radius * Vec3(1.0, 1.0, 1.0);
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
Real CylinderBuilder::object_sdf_value(const Vec3& pos)
{
    return SignDistanceField::sign_distance_cylinder(pos, cylinder_base, cylinder_direction,
                                                     cylinder_radius, cylinder_length);
}

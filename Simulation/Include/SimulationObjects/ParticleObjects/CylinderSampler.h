#ifndef CYLINDER_BUILDER_H
#define CYLINDER_BUILDER_H

#include <Noodle/Core/Particle/ObjectBuilders/ObjectBuilder.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class CylinderBuilder : public ObjectBuilder
{
public:
    CylinderBuilder(DomainParameters* domainParams_,
                    ParticleSamplingParameters* samplingParams_,
                    const Array3_Real& sdf_boundary_) :
        ObjectBuilder(domainParams_, samplingParams_, sdf_boundary_),
        cylinder_initialized(false)
    {
        set_obj_name("cylinder");
    }

    void set_cylinder_params(const Vec3& base_center, const Vec3& direction,
                             Real radius,
                             Real length);

protected:
    virtual std::string get_generator_name() override
    {
        return std::string("CylinderBuilder");
    }

    virtual bool check_all_params() override
    {
        return cylinder_initialized;
    }

    virtual void compute_aabb() override;
    virtual Real object_sdf_value(const Vec3& pos) override;

    Vec3 cylinder_base;
    Vec3 cylinder_direction;
    Real cylinder_radius;
    Real cylinder_length;

    bool cylinder_initialized;
};

#endif // CYLINDER_BUILDER_H
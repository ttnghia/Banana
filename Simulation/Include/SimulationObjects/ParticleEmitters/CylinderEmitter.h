#ifndef CYLINDER_EMITTER_H
#define CYLINDER_EMITTER_H

#include <Noodle/Core/Particle/ParticleEmitters/ParticleEmitter.h>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class CylinderEmitter : public ParticleEmitter
{
public:
    CylinderEmitter(DomainParameters* domainParams_,
                    ParticleSamplingParameters* samplingParams_,
                    const Array3_Real& sdf_boundary_,
                    Real particle_radius_) :
        ParticleEmitter(domainParams_, samplingParams_, sdf_boundary_, particle_radius_),
        cylinder_initialized(false) {}

    void set_cylinder_params(const Vec3& center, const Vec3& direction, Real radius,
                             Real length);

protected:
    virtual bool check_all_params() override
    {
        return (params_initialized && cylinder_initialized);
    }
    virtual void build_emitter() override;

    Vec3 base_center;
    Vec3 emit_direction;
    Real cylinder_radius;
    Real cylinder_length;

    bool cylinder_initialized;
};

#endif // CYLINDER_EMITTER_H

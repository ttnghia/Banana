// solver class
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
template<class RealType>
class FLIPSolver : public ParticleSolver<RealType>
{
public:
    FLIPSolver(const std::shared_ptr<TimeParameters>& timeParams, const std::shared_ptr<DataParameters>& dataParams, const std::shared_ptr<MPMParameters>& simParams) :
        ParticleSolver<RealType>(timeParams, dataParams), m_SimParams(simParams) {}
    virtual ~FLIPSolver() {}

    ////////////////////////////////////////////////////////////////////////////////
    virtual std::string getSolverName() override { return std::string("SPHSolver"); }
    virtual void makeReady() override;
    virtual void advanceFrame() override;
    virtual void saveParticleData() override;
    virtual void saveMemoryState() override;

private:
    RealType computeCFLTimeStep();
    void     advanceVelocity(RealType timeStep);
    void     updateVelocity(RealType timeStep);
    void     moveParticles(RealType timeStep);

    ////////////////////////////////////////////////////////////////////////////////
    std::shared_ptr<FLIPParameters<RealType> > m_SimParams;
    std::unique_ptr<FLIPData<RealType> >       m_SimData = std::make_unique<SPHData<RealType> >();
};


//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void CylinderEmitter::set_cylinder_params(const Vec3& center, const Vec3& direction,
                                          Real radius, Real length)
{
    base_center = center;
    emit_direction = direction;
    cylinder_radius = radius;
    cylinder_length = length;

    cylinder_initialized = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void CylinderEmitter::build_emitter()
{
    __NOODLE_ASSERT(check_all_params());

    monitor.printLog("Building emitter of shape Cylinder:");

    particle_generator = new CylinderBuilder(domainParams, samplingParams, sdf_boundary);

    CylinderBuilder* generator = (CylinderBuilder*)particle_generator;
    generator->set_cylinder_params(base_center, emit_direction, cylinder_radius,
                                   cylinder_length);
    generator->generate_particle(emitter_particles, particle_radius);
}

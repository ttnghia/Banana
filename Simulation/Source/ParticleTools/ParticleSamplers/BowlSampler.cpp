//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BowlBuilder::set_bowl_params(const Vec3& center_, Real max_y_, Real outer_radius_,
                                  Real thickness_)
{
    bowl_center = center_;
    max_y = max_y_;
    outer_radius = outer_radius_;
    thickness = thickness_;
    inner_radius = outer_radius - thickness_;

    ////////////////////////////////////////////////////////////////////////////////
    monitor.printLog("Set bowl center: " + NumberHelpers::toString(bowl_center) +
                     ", max_y = " + NumberHelpers::formatWithCommas(max_y, 5) +
                     ", outer radius = " + NumberHelpers::formatWithCommas(outer_radius, 5) +
                     ", thickness = " + NumberHelpers::formatWithCommas(thickness, 5));

    bowl_initialized = true;
    invalidate_data();
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BowlBuilder::compute_aabb()
{
    obj_bbmin = bowl_center - Vec3(outer_radius, outer_radius, outer_radius);
    obj_bbmax = bowl_center + Vec3(outer_radius, outer_radius, outer_radius);
    obj_bbmax[1] = max_y;

    aabb_ready = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
Real BowlBuilder::object_sdf_value(const Vec3& pos)
{
    return SignDistanceField::sign_distance_bowl(pos, bowl_center, inner_radius,
                                                 outer_radius, thickness, max_y);;
}


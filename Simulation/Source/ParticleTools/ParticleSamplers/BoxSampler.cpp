//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BoxBuilder::set_box_params(const Vec3& box_min_, const Vec3& box_max_)
{
    box_min = box_min_;
    box_max = box_max_;

    ////////////////////////////////////////////////////////////////////////////////
    monitor.printLog("Set box from min = " + NumberHelpers::toString(box_min) + ", to max = " +
                     NumberHelpers::toString(box_max));

    box_initialized = true;
    invalidate_data();
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BoxBuilder::pos_process(Vec_Vec3& particles, Real particle_radius, Real threshold)
{
    Real t = threshold * particle_radius;

    // move the particle near box surface to surface
    for(Vec3& pos : particles)
    {
        for(int i = 0; i < 3; ++i)
        {
            if(pos[i] < box_min[i] + particle_radius + t)
            {
                pos[i] = box_min[i] + particle_radius;
            }

            if(pos[i] > box_max[i] - particle_radius - t)
            {
                pos[i] = box_max[i] - particle_radius;
            }
        }
    }
}
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void BoxBuilder::compute_aabb()
{
    obj_bbmin = box_min;
    obj_bbmax = box_max;

    aabb_ready = true;
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
Real BoxBuilder::object_sdf_value(const Vec3& pos)
{
    return SignDistanceField::sign_distance_box(pos, box_min, box_max);
}




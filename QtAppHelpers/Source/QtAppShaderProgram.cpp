//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void QtAppShaderProgram::addVertexShaderFromResource(const char* fileName)
{
    std::string shaderSouce;
    loadResourceFile(shaderSouce, fileName);

    addVertexShaderFromSource(shaderSouce.c_str());
}


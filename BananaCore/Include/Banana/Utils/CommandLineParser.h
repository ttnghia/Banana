#pragma once

#include <map>
#include <string>
#include <algorithm>

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
namespace Banana
{
//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
class CommandLineParser : public std::map<const std::string, const std::string>
{
public:
    void init(int argc, char** argv)
    {
        for(int arg = 1; arg < argc; ++arg)
        {
            std::string argstr(argv[arg]);
            argstr.erase(std::remove(argstr.begin(), argstr.end(), '-'), argstr.end());

            if(argstr.find('=') == std::string::npos)
                throw std::exception("Invalid stdin: variable assignment must be '...=value' without spacing");

            const std::string tag(argstr.substr(0, argstr.find('=')));   // exception if = not found
            const std::string val(argstr.substr(tag.size() + 1));

            if(find(tag) == end())
                insert(std::make_pair(tag, val));
            else
                throw std::exception("Duplicate command line tag encountered");
        }
    }

    bool get(const std::string& tag, std::string& value)
    {
        auto it = find(tag);

        if(it == end())
            return false;

        value = it->second;
        return true;
    }
};

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
} // end namespace Banana
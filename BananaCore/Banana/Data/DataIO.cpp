//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
void DataBuffer::append(const void* dataPtr, size_t dataSize)
{
    size_t lastSize = m_Buffer.size();
    resize(lastSize + dataSize);
    memcpy((void*)data(lastSize), dataPtr, dataSize);
}

//-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+-+
size_t DataBuffer::getData(void* dataPtr, size_t dataSize, size_t startOffset /*= 0*/)
{
    __BNN_ASSERT(startOffset + dataSize <= m_Buffer.size());
    memcpy(dataPtr, (void*)data(startOffset), dataSize);
    return dataSize;
}

